def demo_chunking(label: str, text: str, target_chunk_tokens: int, overlap: int = 50):
    """对一段文本执行 chunk_text 并逐块打印效果。"""
    print("=" * 70)
    print(f"  {label}")
    print(f"  参数: target_chunk_tokens={target_chunk_tokens}, overlap_tokens={overlap}")
    print(f"  原文长度: {len(text)} 字符")
    print("=" * 70)

    result = chunk_text(text, target_chunk_tokens=target_chunk_tokens, overlap_tokens=overlap)
    print(f"  切分结果: {result.total_chunks} 个 chunk\n")

    for i, chunk in enumerate(result.chunks):
        print(f"  ┌─ Chunk #{i + 1} {'─' * 50}┐")
        print(f"  │  字符: {chunk.char_count},  Token 估算: {chunk.token_estimate}")
        if result.source:
            print(f"  │  来源: {result.source}")
        print("  │─ 内容 ─────────────────────────────────────────────│")
        # 缩进显示内容
        for line in chunk.text.split("\n"):
            print(f"  │  {line}")
        print(f"  └{'─' * 60}┘\n")

    print(f"  [共 {result.total_chunks} 个 chunk]\n")
    return result


